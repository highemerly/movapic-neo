// version 文字列を数値的に比較して降順ソートする（"1.10" > "1.9" を正しく扱う）。
export function sortedReleaseNotes(): ReleaseNote[] {
  const toParts = (v: string) => v.split(".").map((n) => parseInt(n, 10) || 0);
  return [...releaseNotes].sort((a, b) => {
    const pa = toParts(a.version);
    const pb = toParts(b.version);
    const len = Math.max(pa.length, pb.length);
    for (let i = 0; i < len; i++) {
      const diff = (pb[i] ?? 0) - (pa[i] ?? 0);
      if (diff !== 0) return diff;
    }
    return 0;
  });
}
