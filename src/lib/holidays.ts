/** RAW_CSV から `YYYY-MM-DD`（ゼロ埋め）の集合を作る。ヘッダや空行は無視。 */
function parseHolidays(csv: string): Set<string> {
  const set = new Set<string>();
  for (const line of csv.split(/\r?\n/)) {
    const dateStr = line.split(",")[0]?.trim();
    const m = /^(\d{4})\/(\d{1,2})\/(\d{1,2})$/.exec(dateStr ?? "");
    if (!m) continue; // ヘッダ行・空行など
    const [, y, mo, d] = m;
    set.add(`${y}-${mo.padStart(2, "0")}-${d.padStart(2, "0")}`);
  }
  return set;
}

const HOLIDAYS = parseHolidays(RAW_CSV);

/** 指定日が日本の祝日（振替休日・国民の休日を含む）かどうか。 */
export function isJapaneseHoliday(year: number, month: number, day: number): boolean {
  const key = `${year}-${String(month).padStart(2, "0")}-${String(day).padStart(2, "0")}`;
  return HOLIDAYS.has(key);
}
